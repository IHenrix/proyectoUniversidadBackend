class CursoAlumno:
    def __init__(self, id, curso, horas_semanales, creditos, modalidad, alumno, docente):
        self.id = id
        self.curso = curso
        self.horas_semanales = horas_semanales
        self.creditos = creditos
        self.modalidad = modalidad
        self.alumno = alumno
        self.docente = docente